{
	playerX = x;
	playerY = y;
	SetViewportCenteredOnPosition(*playerX, *playerY);
}

//makes sure it's in bounds
bool Map::isInMapBounds(int x, int y)
{
	if (x >= 0 && x < mapSizeX && y >= 0 && y < mapSizeY)
		return true;
	else
		return false;
}