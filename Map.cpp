void Map::PrintMap()
{
	Display::GetInstance()->SetCursorPosition(old_PlayerX, old_PlayerY);
	std::cout << tileDictionary.GetTile(mapData[viewportPositionY + old_PlayerY][viewportPositionX + old_PlayerX]).GetSymbol();

	// Iterate over the viewport and compare with the current map data
	for (int y = 0; y < viewportSizeY; y++) {
		for (int x = 0; x < viewportSizeX; x++) {
			int mapX = viewportPositionX + x;
			int mapY = viewportPositionY + y;

			// Check if the position is within map bounds
			if (isInMapBounds(mapX, mapY)) {
				int tileID = mapData[mapY][mapX];

				if (!(playerX == nullptr) && !(playerY == nullptr))
					if (mapX == *playerX && mapY == *playerY) {
						old_PlayerX = x;
						old_PlayerY = y;
						Display::GetInstance()->SetCursorPosition(x, y);
						std::cout << '@';  // Use the specific player symbol here
						continue;
					}
				// Compare with the previous state of the viewport
					else if (viewport[x][y] != tileID || (old_PlayerX == x && old_PlayerY)) {
						viewport[x][y] = tileID;

						// Update the display only for changed tiles
						Display::GetInstance()->SetCursorPosition(x, y);
						std::cout << tileDictionary.GetTile(tileID).GetSymbol();
						continue;
					}
				// If out of map bounds, use a default tile ID (e.g., 0)
			}
			else {
				viewport[x][y] = 0;
				Display::GetInstance()->SetCursorPosition(x, y);
				std::cout << tileDictionary.GetTile(0).GetSymbol();
				continue;
			}
		}
	}

	Display::GetInstance()->DrawBorderAroundMainDisplay();
	Display::GetInstance()->PlaceCursorAtBottom();
}

